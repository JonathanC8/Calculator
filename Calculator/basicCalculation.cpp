#include <iostream>
#include "main.h"
using namespace std;

double addition(double a, double b) {
	double output;
	output = a + b;
	return output;
}

double subtraction(double a, double b) {
	double output;
	output = a - b;
	return output;
}

double multiplication(double a, double b) {
	double output;
	output = a * b;
	return output;
}

double division(double a, double b) {
	double output;
	output = a / b;

	return output;
}
